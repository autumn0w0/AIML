marks = [90, 85, 78, 92, 88]
print(marks[1:4])  # Slicing from index 1 to 3
print(marks[:3])   # Slicing from the beginning to index 2
print(marks[2:])   # Slicing from index 2 to the end
print(marks[-3:-1])  # Slicing the last three elements
print(marks[2:len(marks)])  # Slicing from index 2 to the end using len() function